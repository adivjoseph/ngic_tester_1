void ph_dumpPkt(char *pkt, int length){

    int i,j,k;
    for (i = 0, j = 0; j< length; j++) {
        if (i == 0) {
            printf("\n%04x  ", j);
        }
        printf("%02x  ", pkt[j]);
        i++;
        if (i > 15) {
            i = 0;
        }
    }
}

int simm_mme_init(){

    topology_port_t *sim_mmePort, *cpPort;
    int i;

    sim_mmePort = &g_topology.ports[SIM_MME];
    cpPort = &g_topology.ports[CP_S11];
    //copy mac addresses
    for (i = 0; i < 6; i++) {
        //dest mac
        createSessionRequestPkt[i] = cpPort->mac[i];
        modifyBearerRequestPkt[i] = cpPort->mac[i];
        //src mac
        createSessionRequestPkt[i + 6] = sim_mmePort->mac[i];
        modifyBearerRequestPkt[i +6]   = sim_mmePort->mac[i];
    }


    return 0;

}



